export const getBagsByCategory = (subcategory: string): BagProduct[] => {
  if (subcategory === 'all') return bagsProducts;
  return bagsProducts.filter(bag => bag.subcategory === subcategory);
};

export const getRelatedBags = (currentBagId: number, limit: number = 4): BagProduct[] => {
  const currentBag = bagsProducts.find(bag => bag.id === currentBagId);
  if (!currentBag) return [];

  // Get bags from the same subcategory
  const sameCategoryBags = bagsProducts.filter(
    bag => bag.subcategory === currentBag.subcategory && bag.id !== currentBagId
  );

  // If not enough, add random bags
  if (sameCategoryBags.length < limit) {
    const otherBags = bagsProducts.filter(
      bag => bag.subcategory !== currentBag.subcategory && bag.id !== currentBagId
    );
    return [...sameCategoryBags, ...otherBags].slice(0, limit);
  }

  return sameCategoryBags.slice(0, limit);
};
