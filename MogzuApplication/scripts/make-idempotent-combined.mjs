// Transform supabase/_combined/all_migrations.sql into an idempotent
// variant safe to re-run against a partially-applied project.
//
// Usage: node scripts/make-idempotent-combined.mjs

import { readFile, writeFile } from 'node:fs/promises'
import { resolve } from 'node:path'

const IN = resolve('supabase/_combined/all_migrations.sql')
const OUT = resolve('supabase/_combined/all_migrations_idempotent.sql')

let sql = await readFile(IN, 'utf8')

// 1. CREATE TABLE public.X  →  CREATE TABLE IF NOT EXISTS public.X
sql = sql.replace(/\bCREATE TABLE (?!IF NOT EXISTS)/g, 'CREATE TABLE IF NOT EXISTS ')

// 2. CREATE INDEX foo / CREATE UNIQUE INDEX foo  →  ... IF NOT EXISTS foo
sql = sql.replace(/\bCREATE INDEX (?!IF NOT EXISTS)/g, 'CREATE INDEX IF NOT EXISTS ')
sql = sql.replace(
  /\bCREATE UNIQUE INDEX (?!IF NOT EXISTS)/g,
  'CREATE UNIQUE INDEX IF NOT EXISTS ',
)

// 3. CREATE VIEW name  →  CREATE OR REPLACE VIEW name (skip if already OR REPLACE)
sql = sql.replace(/\bCREATE VIEW\b/g, 'CREATE OR REPLACE VIEW')

// 4. CREATE POLICY "name" ON public.table  →  prepend DROP POLICY IF EXISTS
//    Captures the policy name + target relation across one or two lines.
sql = sql.replace(
  /\bCREATE POLICY\s+("[^"]+"|\w+)\s+ON\s+(public\.\w+)/g,
  (_, name, target) =>
    `DROP POLICY IF EXISTS ${name} ON ${target};\nCREATE POLICY ${name} ON ${target}`,
)

// 5. CREATE TRIGGER name ... ON public.table  →  prepend DROP TRIGGER IF EXISTS
//    Trigger bodies span multiple lines; capture name + ON-clause table.
sql = sql.replace(
  /\bCREATE TRIGGER\s+(\w+)\s*\n?\s*(?:BEFORE|AFTER|INSTEAD OF)[^;]*?\bON\s+(public\.\w+)/g,
  (match, name, target) =>
    `DROP TRIGGER IF EXISTS ${name} ON ${target};\n${match}`,
)

// 6. ALTER TABLE ... ADD CONSTRAINT name ...  →  prepend
//    `ALTER TABLE table DROP CONSTRAINT IF EXISTS name;`. Handles both
//    single-clause and multi-clause ALTER TABLE statements.
sql = sql.replace(
  /ALTER TABLE\s+(public\.\w+)([\s\S]*?);\s*$/gm,
  (block, table) => {
    const constraintNames = [...block.matchAll(/ADD CONSTRAINT\s+(\w+)/g)].map((m) => m[1])
    if (constraintNames.length === 0) return block
    const drops = constraintNames
      .map((c) => `ALTER TABLE ${table} DROP CONSTRAINT IF EXISTS ${c};`)
      .join('\n')
    return `${drops}\n${block}`
  },
)

// 7. ADD COLUMN (without IF NOT EXISTS) inside ALTER TABLE  →
//    ADD COLUMN IF NOT EXISTS. Most migrations already use the safe
//    form; this catches the few that don't (e.g. migration 19).
sql = sql.replace(/\bADD COLUMN (?!IF NOT EXISTS)/g, 'ADD COLUMN IF NOT EXISTS ')

// 7b. CREATE TYPE has no IF NOT EXISTS in Postgres. Wrap each enum
//     declaration in a DO block that swallows 42710 (duplicate_object).
sql = sql.replace(
  /^CREATE TYPE\s+(public\.\w+)\s+AS ENUM\s*\(([\s\S]*?)\);/gm,
  (_, typeName, body) => {
    const escaped = `CREATE TYPE ${typeName} AS ENUM (${body});`.replace(/'/g, "''")
    return `DO $idem$ BEGIN\n  EXECUTE '${escaped}';\nEXCEPTION WHEN duplicate_object THEN NULL;\nEND $idem$;`
  },
)

// 8. INSERT INTO seed rows that lack ON CONFLICT — leave alone. The two
//    known seeds (Mogzu Direct vendor in migration 25, AI agents in
//    migration 28) already include ON CONFLICT DO NOTHING.

const banner = `-- Idempotent variant — safe to re-run against a partially-applied project.
-- Auto-generated by scripts/make-idempotent-combined.mjs at ${new Date().toISOString()}.
-- Do not edit by hand; re-run the generator.

`

await writeFile(OUT, banner + sql, 'utf8')
console.log(`Wrote ${OUT}`)
console.log(`Length: ${sql.length} chars`)
